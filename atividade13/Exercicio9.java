package atividade13;

public class Exercicio9 {
	
    public static double calcularArea(double base, double altura) {
        return base * altura;
    }

    public static void main(String[] args) {
        System.out.println(calcularArea(5.0, 3.0));
        System.out.println(calcularArea(7.5, 4.2));
    }
}
