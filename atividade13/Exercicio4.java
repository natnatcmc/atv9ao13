package atividade13;

public class Exercicio4 {
	
    public static double calcularMedia(double num1, double num2, double num3) {
        return (num1 + num2 + num3) / 3;
    }

    public static void main(String[] args) {
        System.out.println(calcularMedia(3.0, 4.0, 5.0));
    }
}
